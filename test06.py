# operator ตัวดำเนินการ คือ เครื่องหมายที่ใฃ้ในการประมวลผลข้อมูล
# 1. Arithmetic operator เครื่องหมายคำนวณ
# (+, -, *, /,% mod, // floor, ** eponential)
print(10 + 3)
print(10 - 3)
print(10 + 3)
print(10 * 3)
print(10 / 3)
print(10 % 3)#หารเอาเศษ
print(10 // 3)#หารเอาส่วน
print(10 ** 3)
print('*************************************')
# 2. comparison operator เครื่องหมายเปรียบเทียบ (==,!=,>,<,>=,<=)
# ผลลัพธ์ที่ได้จะมีแค่ True หรือ False
print(10 == 3)
print(10 != 3)
print('bangna' >= 'bangpoo')
print('Thailand' < 'THAILAND')
print('******************')
# 3. Logical operator เครื่องหมาย ตรรกะ not,and,or
# ข้อมูลที่จะใฃ้กับเครื่องหมายนี้จะต้องเป็น true/false และผลลัพธ์ที่ได้จากการใฃ้เครื่องหมาย true/false
print(not true)
print(not false)
print('-----')
print(true and true) #true
print(true and false)
print(false and true)
print(false and false) #false
print(false and true)







