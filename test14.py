# if-elif-else

score = 78

if score >= 80: #colon อย่าลืม
    print('A')
elif score >= 70: #colon อย่าลืม
    print('B')
elif score >= 60: #colon อย่าลืม
    print('C')
elif score >= 50: #colon อย่าลืม
    print('D') 
else: #colon อย่าลืม *** elif สุดท้าบจะเป็น elif ก็ได้
    print('F')

    print('Bye bye...')
    print('Ei ei...')
